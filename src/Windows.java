/**
 * Clase {@code Windows}.
 * <p>
 * Representa el sistema operativo Windows como un tipo de {@link Programa}.
 * Permite simular la instalacion del sistema operativo Windows en la computadora.
 * </p>
 */
public class Windows extends Programa {

    /**
     * Constructor que crea una nueva instancia de {@code Windows}.
     * Inicializa el nombre del programa y su precio.
     */
    public Windows() {
        super("Windows 10", 500.0);
    }

    /**
     * @return representacion en texto del programa con nombre y precio.
     */
    @Override
    public String toString() {
        return super.toString();
    }
}
